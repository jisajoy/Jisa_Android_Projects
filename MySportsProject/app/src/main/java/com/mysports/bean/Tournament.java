public class Tournament {
    private String mTournamentPlace;
    private String mTournamentDate;
    private String mTournamentMainHeading;
    private String mTournamentImage;

    public String getmTournamentPlace() {
        return mTournamentPlace;
    }

    public void setmTournamentPlace(String mTournamentPlace) {
        this.mTournamentPlace = mTournamentPlace;
    }

    public String getmTournamentDate() {
        return mTournamentDate;
    }

    public void setmTournamentDate(String mTournamentDate) {
        this.mTournamentDate = mTournamentDate;
    }

    public String getmTournamentMainHeading() {
        return mTournamentMainHeading;
    }

    public void setmTournamentMainHeading(String mTournamentMainHeading) {
        this.mTournamentMainHeading = mTournamentMainHeading;
    }

    public String getmTournamentImage() {
        return mTournamentImage;
    }

    public void setmTournamentImage(String mTournamentImage) {
        this.mTournamentImage = mTournamentImage;
    }
}
