public class VenueCartBean {
    private String mVenueName;
    private String mVenueDate;
    private String mVenueTime;
    private String mSession;
    private String mVenuePrice;

    public String getmVenueName() {
        return mVenueName;
    }

    public void setmVenueName(String mVenueName) {
        this.mVenueName = mVenueName;
    }

    public String getmVenueDate() {
        return mVenueDate;
    }

    public void setmVenueDate(String mVenueDate) {
        this.mVenueDate = mVenueDate;
    }

    public String getmVenueTime() {
        return mVenueTime;
    }

    public void setmVenueTime(String mVenueTime) {
        this.mVenueTime = mVenueTime;
    }

    public String getmSession() {
        return mSession;
    }

    public void setmSession(String mSession) {
        this.mSession = mSession;
    }

    public String getmVenuePrice() {
        return mVenuePrice;
    }

    public void setmVenuePrice(String mVenuePrice) {
        this.mVenuePrice = mVenuePrice;
    }
}
